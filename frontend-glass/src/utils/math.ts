export function gaussRand(): number {
  let u = 0, v = 0;
  while (u === 0) u = Math.random();
  while (v === 0) v = Math.random();
  return Math.sqrt(-2.0 * Math.log(u)) * Math.cos(2.0 * Math.PI * v);
}

export function generateRandomTransaction(): number[] {
  const time = Math.random() * 172800;
  const amount = Math.random() * 2500 + 1; // 1 to 2500
  const pca = Array.from({ length: 28 }, () => gaussRand());
  // Set time, V1..V28, Amount, and delta_time (defaults to 0 for single run)
  return [time, ...pca, amount, 0.0];
}

